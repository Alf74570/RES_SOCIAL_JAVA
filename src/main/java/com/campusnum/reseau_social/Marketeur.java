package com.campusnum.reseau_social;

public class Marketeur extends Employé{
	
	public Marketeur(
			String nom, 
			String prenom, 
			int dateNaiss, 
			String natio,
			int salaire,
			String duréeContrat) {
	
		this.nom = nom;
		this.prenom = prenom;
		this.dateNaiss = dateNaiss;
		this.natio = natio;
		this.salaire = salaire;
		this.duréeContrat = duréeContrat;
	}

}
